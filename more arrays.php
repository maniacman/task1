Заполнение массивов
1) Заполните массив следующим образом: в первый элемент запишите 'x', во второй 'xx', в третий 'xxx' и так далее.
<?php
$num = 10; //необходимое число элементов массива
for ($i = 1; $i <= $num; $i++) {
	$arr[] = str_repeat('x', $i);
}
print_r($arr);
?>

2) С помощью двух вложенных циклов заполните массив следующим образом: в первый элемент запишите '1', во второй '22', в третий '333' и так далее.
// одним циклом
<?php
$num = 10; //необходимое число элементов массива
for ($i = 1; $i <= $num; $i++) {
	$arr[] = str_repeat($i, $i);
}
print_r($arr);
?>
// двумя циклами
<?php
$num = 10; //необходимое число элементов массива
for ($i = 1; $i <= $num; $i++) {
	for ($j = 1; $j <= $i; $j++) {
		$arr[$i - 1] .= $i;
	}
}
print_r($arr);
?>
3) Сделайте функцию arrayFill, которая будет заполнять массив заданными значениями. Первым параметром функция принимает значение, 
которым заполнять массив, а вторым - сколько элементов должно быть в массиве. Пример: arrayFill('x', 5) сделает массив ['x', 'x', 'x', 'x', 'x'].
<?php
function arrayFill($param, $val) {
	for ($i = 1; $i <= $val; $i++){
		$arr[] = $param;
	}
	return $arr;
}
$param = 'x';
$val = 5;
print_r(arrayFill($param, $val));
?>


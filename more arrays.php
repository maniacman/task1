Многомерные массивы
5) Дан двухмерный массив с числами, например [[1, 2, 3], [4, 5], [6]]. Найдите сумму элементов этого массива. Массив, конечно же, может быть произвольным.
<?php
$arr = [[1, 2, 3], [4, 5], [6]];
$sum = 0;
foreach ($arr as $a) {
	$sum += array_sum($a);
}
echo 'Сумма элементов массива = ' . $sum;
?>

6) Дан трехмерный массив с числами, например [[[1, 2], [3, 4]], [[5, 6], [7, 8]]]. Найдите сумму элементов этого массива. 
Массив, конечно же, может быть произвольным.
<?php
$arr = [[[1, 2], [3, 4]], [[5, 6], [7, 8]]];
$sum = 0;
foreach ($arr as $a) {
	foreach ($a as $b) {
		$sum += array_sum($b);
	}
}
echo 'Сумма элементов массива = ' . $sum;
?>

